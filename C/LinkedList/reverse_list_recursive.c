/*******************************************************************************
Header Files
*******************************************************************************/
#include <stdio.h>
#include <stdlib.h>

/*******************************************************************************
List Structure
*******************************************************************************/
struct Node {
  int data;
  struct Node* next;
};

/*******************************************************************************
Global Variables
*******************************************************************************/
struct Node* head;

/*******************************************************************************
Function Prototypes
*******************************************************************************/
void Insert(int x);
void Reverse(struct Node* p);
void Print();

/*******************************************************************************
Main
*******************************************************************************/
int main() {
  head = NULL;
  Insert(2);
  Insert(4);
  Insert(6);
  Insert(5);
  Print();
  Reverse(head);
  printf("Reversing List...\n");
  Print();
}

/*******************************************************************************
Insert Function
*******************************************************************************/
void Insert(int x) {
  struct Node* temp = (struct Node*)malloc(sizeof(struct Node));
  temp->data = x;
  temp->next = head;
  head = temp;
}

/*******************************************************************************
Reverse Function
*******************************************************************************/
void Reverse(struct Node* p) {
  if (p->next == NULL) {
    head = p;
    return;
  }
  Reverse(p->next);
  struct Node* q = p->next;
  q->next = p ;
  p->next = NULL;
}

/*******************************************************************************
Print Function
*******************************************************************************/
void Print() {
  struct Node* temp = head;
  printf("List is:");
  while (temp != NULL) {
    printf(" %d", temp->data);
    temp = temp->next;
  }
  printf("\n");
}
